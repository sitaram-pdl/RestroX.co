export const Bills=async()=>{
    try{    
        const {data} = await axios.get(dummyData, { headers: { 'app-id': APP_ID } });
        const dateList = []
        let arra={}
        const dummy=  data.data.map((dat2,index)=>
        {   if(!arra[new Date(dat2.publishDate).toDateString()])
            {
            arra[new Date(dat2.publishDate).toDateString()]=[]
            dateList.push(new Date(dat2.publishDate).toDateString())
            }
        arra[new Date(dat2.publishDate).toDateString()].push(dat2)
        return arra
        }
        )
        return {arra,dateList}
    }
    catch(e){
        return e
    }
}
